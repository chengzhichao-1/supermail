<template>
  <div class="tab-control">
      <div v-for="(item, index, value) in titles" :key="value" 
        class="tab-control-item" 
        :class="{active: index === currentIndex}"
        @click="itemClick(index)">
          <span>{{item}}</span>
      </div>
  </div>
</template>

<script>
export default {
    name: "TabControl",
    props: {
        titles: {
            type: Array,
            default() {
                return []
            }
        }
    },
    data(){
        return {
            currentIndex: 0,

        }
    },
    methods: {
        itemClick(index) {
            this.currentIndex = index;
            this.$emit('tabClick', index) //里往外传数据
        }
    }
}
</script>

